namespace ElevatorSystem.Domain.Enums;

/// <summary>
/// Represents the direction of elevator movement or request
/// </summary>
public enum Direction
{
    /// <summary>
    /// No specific direction (idle state)
    /// </summary>
    None,

    /// <summary>
    /// Upward direction
    /// </summary>
    Up,

    /// <summary>
    /// Downward direction
    /// </summary>
    Down
}
